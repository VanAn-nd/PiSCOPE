static inline void WR_DATA() {
    sleep_us(1);
    gpio_put(LCD_DC, 1); 
    //sleep_us(1);
}

static inline void WR_CMD() {
    sleep_us(1);
    gpio_put(LCD_DC, 0);
    //sleep_us(1);
}

static inline void lcd_write_cmd(PIO pio, uint sm, const uint8_t *cmd, size_t count) {
    //st7789_lcd_wait_idle(LCD_pio, LCD_sm);
    WR_CMD();
    st7789_lcd_put(LCD_pio, LCD_sm, *cmd++);
    if (count >= 2) {
        st7789_lcd_wait_idle(LCD_pio, sm);
        WR_DATA();
        for (size_t i = 0; i < count - 1; ++i)
            st7789_lcd_put(LCD_pio, LCD_sm, *cmd++);
    }
    //st7789_lcd_wait_idle(LCD_pio, LCD_sm);
    WR_DATA();
}

static inline void lcd_init(PIO pio, uint sm, const uint8_t *init_seq) {
    const uint8_t *cmd = init_seq;
    while (*cmd) {
        lcd_write_cmd(LCD_pio, LCD_sm, cmd + 2, *cmd);
        sleep_ms(*(cmd + 1) * 5);
        cmd += *cmd + 2;
    }
}

void LCD_writeData(uint8_t data)
{
    //st7789_lcd_wait_idle(LCD_pio, LCD_sm);
    WR_DATA();
    st7789_lcd_put(LCD_pio, LCD_sm, data);
}

void LCD_writeBlockData(uint8_t *data, uint16_t len)
{
    //st7789_lcd_wait_idle(LCD_pio, LCD_sm);
    WR_DATA();
    for (size_t i = 0; i < len; ++i)
    {
        st7789_lcd_put(LCD_pio, LCD_sm, *data++);
        st7789_lcd_put(LCD_pio, LCD_sm, *data++);
    }
}

void LCD_writeCmd(uint8_t cmd)
{
    //st7789_lcd_wait_idle(LCD_pio, LCD_sm);
    WR_CMD();
    st7789_lcd_put(LCD_pio, LCD_sm, cmd);
}

static void LCD_SetAddressWindow(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2)
{
    uint8_t xa[4] = {x1>>8, x1&0xFF, x2>>8, x2&0xFF};
    uint8_t ya[4] = {y1>>8, y1&0xFF, y2>>8, y2&0xFF};
    //uint8_t ya[4] = {(y1+80)>>8,(uint8_t)(y1+80)&0xFF, (y2+80)>>8,(uint8_t)(y2+80)&0xFF};
    WR_CMD();
        st7789_lcd_put(LCD_pio, LCD_sm, ST7789_CASET);
    WR_DATA();
        st7789_lcd_put(LCD_pio, LCD_sm, xa[0]);
        st7789_lcd_put(LCD_pio, LCD_sm, xa[1]);
        st7789_lcd_put(LCD_pio, LCD_sm, xa[2]);
        st7789_lcd_put(LCD_pio, LCD_sm, xa[3]);
    WR_CMD();
        st7789_lcd_put(LCD_pio, LCD_sm, ST7789_RASET);
    WR_DATA();
        st7789_lcd_put(LCD_pio, LCD_sm, ya[0]);
        st7789_lcd_put(LCD_pio, LCD_sm, ya[1]);
        st7789_lcd_put(LCD_pio, LCD_sm, ya[2]);
        st7789_lcd_put(LCD_pio, LCD_sm, ya[3]);
    WR_CMD();
        st7789_lcd_put(LCD_pio, LCD_sm, ST7789_RAMWR);
}

void LCD_DrawPixel(uint16_t x, uint16_t y, uint16_t color)
{
	//CS_ACTIVE;
    LCD_SetAddressWindow(x, y, x, y);
    WR_DATA();
    st7789_lcd_put(LCD_pio, LCD_sm, color >> 8);
    st7789_lcd_put(LCD_pio, LCD_sm, color & 0xFF);
    //CS_IDE;
}

void LCD_WriteChar(uint16_t x, uint16_t y, char ch, FontDef font, uint16_t color, uint16_t bgcolor)
{
    uint32_t i, b, j;
    if (ch == '.')
    {
        LCD_SetAddressWindow(x, y, x+4-1, y+font.height-1);
        WR_DATA();
        for(i = 0; i < font.height; i++)
        {
            b = font.data[(ch - 32) * font.height + i];  // b = pos of HEX in font array
            for(j=2; j < 6; j++)
            {
                if((b << j) & 0x8000)
                {
                    st7789_lcd_put(LCD_pio, LCD_sm, color >> 8);
                    st7789_lcd_put(LCD_pio, LCD_sm, color & 0xFF);
                }
                else
                {
                    st7789_lcd_put(LCD_pio, LCD_sm, bgcolor >> 8);
                    st7789_lcd_put(LCD_pio, LCD_sm, bgcolor & 0xFF);
                }
            }
        }
    }
    else{
        LCD_SetAddressWindow(x, y, x+font.width-1, y+font.height-1);
        WR_DATA();
        for(i = 0; i < font.height; i++)
        {
            b = font.data[(ch - 32) * font.height + i];  // b = pos of HEX in font array
            for(j=0; j < font.width; j++)
            {
                if((b << j) & 0x8000)
                {
                    st7789_lcd_put(LCD_pio, LCD_sm, color >> 8);
                    st7789_lcd_put(LCD_pio, LCD_sm, color & 0xFF);
                }
                else
                {
                    st7789_lcd_put(LCD_pio, LCD_sm, bgcolor >> 8);
                    st7789_lcd_put(LCD_pio, LCD_sm, bgcolor & 0xFF);
                }
            }
        }
    }
}


void LCD_WriteString(uint16_t x, uint16_t y, const char *str, FontDef font, uint16_t color, uint16_t bgcolor)
{
	//CS_ACTIVE;
	while (*str)
	{
        if (x + font.width >= LCD_WIDTH)
		{
			x = 0;
			y += font.height;
			if (y + font.height >= LCD_HEIGHT)
			{
				break;
			}
			//if (*str == '0')// skip spaces in the beginning of the new line
			//{
			//	str++;
			//	continue;
			//}
		}
        LCD_WriteChar(x, y, *str, font, color, bgcolor);
        str++;
        x += font.width;
	}
	//CS_IDE;
}

void LCD_FillScreen(uint16_t color){
	//CS_ACTIVE;
	uint16_t i, j;
    uint8_t data[2] = {color >> 8, color & 0xFF};
	LCD_SetAddressWindow(0, 0, LCD_WIDTH - 1, LCD_HEIGHT - 1);
    WR_DATA();
	for (i = 0; i < LCD_WIDTH; i++)
		for (j = 0; j < LCD_HEIGHT; j++) {
			st7789_lcd_put(LCD_pio, LCD_sm, data[0]);
            st7789_lcd_put(LCD_pio, LCD_sm, data[1]);
		}
	//CS_IDE;
}


void LCD_FillRectangle(uint16_t x, uint16_t y, uint16_t w, uint16_t h, uint16_t color)
{
    if ((x + w - 1) >= LCD_WIDTH)   w = LCD_WIDTH - x;
    if ((y + h - 1) >= LCD_HEIGHT)  h = LCD_HEIGHT - y;

    LCD_SetAddressWindow(x, y, x + w - 1, y + h - 1);
    WR_DATA();
	for (y = h; y > 0; y--)
    {
        for (x = w; x > 0; x--)
        {
            st7789_lcd_put(LCD_pio, LCD_sm, color >> 8);
            st7789_lcd_put(LCD_pio, LCD_sm, color & 0xFF);
        }
    }
}

void LCD_DrawFastVLine(int16_t x, int16_t y1, int16_t y2, uint16_t color)
{
	LCD_SetAddressWindow(x, y1, x, y2);
	for (int u=y2-y1; u>=0; u--) {
        st7789_lcd_put(LCD_pio, LCD_sm, color >> 8);
        st7789_lcd_put(LCD_pio, LCD_sm, color & 0xFF);
    }
}

void LCD_DrawFastHLine(int16_t x1, int16_t x2, int16_t y, uint16_t color)
{
	LCD_SetAddressWindow(x1, y, x2, y);
	for (int u=x2-x1; u>=0; u--){
        st7789_lcd_put(LCD_pio, LCD_sm, color >> 8);
        st7789_lcd_put(LCD_pio, LCD_sm, color & 0xFF);
    }
}

void LCD_Init()
{
    uint offset = pio_add_program(LCD_pio, &st7789_lcd_program);
    st7789_lcd_program_init(LCD_pio, LCD_sm, offset, PIN_DIN, PIN_CLK, SERIAL_CLK_DIV);
    gpio_init(LCD_RST);
    gpio_set_dir(LCD_RST, GPIO_OUT);    
    gpio_init(LCD_DC);
    gpio_set_dir(LCD_DC, GPIO_OUT);
    //LCD Reset
    gpio_put(LCD_RST, 1);
    //LCD Init
    lcd_init(LCD_pio, LCD_sm, lcd_init_seq);
    //LCD Clear
    LCD_FillScreen(BLACK);
}


















